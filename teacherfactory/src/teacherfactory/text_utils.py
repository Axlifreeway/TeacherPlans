"""
Текстовые утилиты, общие для индексации и retrieval.

Главное правило: токенизация и регулярки для кодов компетенций должны быть
ровно в одном месте. Раньше `_tokenize` дублировался в indexer.py и
generator.py — расхождение между ними молча ломает BM25-поиск кодов.
"""

import re

# Коды компетенций: «ОК 01», «ПК 1.2», «ОК01» — захватываем все варианты.
COMPETENCY_RE = re.compile(r"(?:ОК|ПК)\s*\d+(?:\.\d+)*")

# Запросы-перечисления: «перечисли все ОК», «какие дисциплины», «список ПК».
LIST_INTENT_RE = re.compile(
    r"(перечисл|список|все\s+(?:ок|пк)|полн|все\s+компетенц|"
    r"какие\s+дисципл|дисципл\w+\s+направл)",
    re.IGNORECASE,
)

# Код специальности СПО: «09.01.03», «10.02.05» и т.п.
SPECIALTY_CODE_RE = re.compile(r"\d{2}\.\d{2}\.\d{2}")

# Нормализация компактных кодов: «ОК01» → «ОК 01», «ПК1.2» → «ПК 1.2».
_COMPACT_CODE_RE = re.compile(r"([ОП][КМ])\s*(\d)")


def tokenize(text: str) -> list[str]:
    """Токенизация для BM25 с нормализацией кодов компетенций."""
    text = _COMPACT_CODE_RE.sub(r"\1 \2", text)
    return text.lower().split()


def normalize_code(code: str) -> str:
    """«ОК 01» / «ОК01» / «ОК 1» → один и тот же ключ для сравнения."""
    return re.sub(r"\s+", "", code)
